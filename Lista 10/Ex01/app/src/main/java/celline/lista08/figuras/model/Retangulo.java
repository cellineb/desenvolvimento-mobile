package celline.lista08.figuras.model;

public class Retangulo {

    private float base;
    private float altura;

    public Retangulo() {
        this.base = base;
        this.altura = altura;
    }


    public float getBase() {
        return base;
    }
    public void setBase(float base) {
        this.base = base;
    }

    public float getAltura() {
        return altura;
    }
    public void setAltura(float altura) {
        this.altura = altura;
    }


}
